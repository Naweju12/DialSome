package com.github.biltudas1.dialsome;

import android.app.NotificationChannel;
import android.app.NotificationManager;
import android.app.Notification;
import android.app.PendingIntent;
import android.content.Context;
import android.content.Intent;
import android.os.Build;
import android.util.Log;
import androidx.core.app.NotificationCompat;
import com.google.firebase.messaging.FirebaseMessagingService;
import com.google.firebase.messaging.RemoteMessage;
import java.util.Map;
import android.media.AudioAttributes;
import android.media.RingtoneManager;
import android.net.Uri;

public class MyFirebaseMessagingService extends FirebaseMessagingService {
    private static final String TAG = "DialSomeFCM";
    private static final int INCOMING_CALL_NOTIF_ID = 1001;

    // Native method to notify the C++ Backend that a message arrived
    public native void onCallMessageReceive(String roomId, String email, String roomName);
    public native void onCallMessageEnd(String email);
    private native void notifyNewToken(String token);

    /**
     * Called when a new token is generated.
     */
    @Override
    public void onNewToken(String token) {
        super.onNewToken(token);
        notifyNewToken(token);
        // In a real scenario, you'd send this to your /users/update-fcm endpoint
    }

    /**
     * Called when a message is received from the FastAPI backend.
     */
    @Override
    public void onMessageReceived(RemoteMessage remoteMessage) {
        super.onMessageReceived(remoteMessage);

        if (remoteMessage.getData().size() > 0) {
            Map<String, String> data = remoteMessage.getData();
            String type = data.get("type");
            if ("incoming_call".equals(type)) {
                String roomId = data.get("room_id"); // The UUIDv7/v4 generated by backend
                String callerEmail = data.get("caller_email");
                String callerName = data.get("caller_name");

                Log.d(TAG, "FCM Signal: " + type + " Room: " + roomId + " From: " + callerEmail + "(" + callerName + ")");

                // Block check — silently drop if caller is blocked
                if (CallStateManager.isBlocked(callerEmail)) {
                    Log.d(TAG, "Blocked incoming call from: " + callerEmail + ". Ignoring.");
                    return;
                }

                CallStateManager.isIncomingCallRinging = true;
                wakeUpApp(callerEmail, roomId, callerName);

                try {
                    onCallMessageReceive(roomId, callerEmail, callerName);
                } catch (UnsatisfiedLinkError e) {
                    Log.d(TAG, "Unable to notify C++ about incoming call, C++ is dead");
                }
            } else if ("end_call".equals(type)) {
                NotificationManager nm = (NotificationManager) getSystemService(Context.NOTIFICATION_SERVICE);
                nm.cancel(INCOMING_CALL_NOTIF_ID);

                String email = data.get("to");
                Log.d(TAG, "FCM Signal: " + type + " to: " + email);
                String callerName = data.get("caller_name");
                String callerEmail = data.get("caller_email");

                // Only show missed call if the call was NEVER answered
                if (CallStateManager.isIncomingCallRinging && !CallStateManager.isCallActive) {
                    String channelId = "MissedCalls";

                    // Create the NotificationChannel for Android O and above
                    if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.O) {
                        NotificationChannel channel = new NotificationChannel(
                                channelId, 
                                "Missed Calls", 
                                NotificationManager.IMPORTANCE_DEFAULT
                        );
                        nm.createNotificationChannel(channel);
                    }

                    NotificationCompat.Builder missedCallBuilder = new NotificationCompat.Builder(this, channelId)
                            .setSmallIcon(android.R.drawable.ic_menu_call)
                            .setContentTitle("Missed Call")
                            .setContentText("You missed a call from " + callerName)
                            .setAutoCancel(true)
                            .setPriority(NotificationCompat.PRIORITY_DEFAULT);

                    nm.notify(1002, missedCallBuilder.build());

                    // Save to SecureStorage if the C++ app is NOT running (background/dead)
                    if (!CallStateManager.isAppRunning) {
                        try {
                            SecureStorage storage = new SecureStorage(this);
                            String historyJson = storage.getData("call_history");
                            org.json.JSONArray historyArray;
                            if (historyJson == null || historyJson.isEmpty()) {
                                historyArray = new org.json.JSONArray();
                            } else {
                                historyArray = new org.json.JSONArray(historyJson);
                            }

                            // Create the new missed call log
                            org.json.JSONObject logObj = new org.json.JSONObject();
                            logObj.put("email", callerEmail);
                            logObj.put("name", callerName != null ? callerName : callerEmail);
                            logObj.put("isIncoming", true);
                            logObj.put("isMissed", true);
                            
                            // Current time in ISO 8601 format
                            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd'T'HH:mm:ss", java.util.Locale.US);
                            String isoTimestamp = sdf.format(new java.util.Date());
                            logObj.put("timestamp", isoTimestamp);

                            // Prepend to array
                            org.json.JSONArray newArray = new org.json.JSONArray();
                            newArray.put(logObj);
                            for (int i = 0; i < historyArray.length(); i++) {
                                newArray.put(historyArray.get(i));
                            }

                            // Limit to 50
                            while (newArray.length() > 50) {
                                newArray.remove(newArray.length() - 1);
                            }

                            storage.saveData("call_history", newArray.toString());
                            Log.d(TAG, "Missed call saved to SecureStorage call_history from background.");
                        } catch (Exception e) {
                            Log.e(TAG, "Failed to save missed call in background: " + e.getMessage());
                        }
                    }
                }

                // Reset the state for the next time a call comes in
                CallStateManager.isIncomingCallRinging = false;
                CallStateManager.isCallActive = false;

                try {
                    onCallMessageEnd(email);
                } catch (UnsatisfiedLinkError e) {
                    Log.d(TAG, "Unable to end call, C++ is dead");
                }
            }
        }
    }

    private void wakeUpApp(String callerEmail, String roomId, String roomName) {
        NotificationManager notificationManager = (NotificationManager) getSystemService(Context.NOTIFICATION_SERVICE);
        String channelId = "IncomingCalls";

        // Create Intent for the "Accept" action
        Intent acceptIntent = new Intent(this, MainActivity.class);
        acceptIntent.setAction("ACCEPT_CALL");
        acceptIntent.putExtra("room_id", roomId);
        acceptIntent.putExtra("caller_email", callerEmail);
        acceptIntent.putExtra("caller_name", roomName);
        acceptIntent.addFlags(Intent.FLAG_ACTIVITY_NEW_TASK | Intent.FLAG_ACTIVITY_CLEAR_TOP | Intent.FLAG_ACTIVITY_SINGLE_TOP);

        PendingIntent acceptPending = PendingIntent.getActivity(
            this, 10, acceptIntent, 
            PendingIntent.FLAG_UPDATE_CURRENT | PendingIntent.FLAG_IMMUTABLE
        );

        // Create Intent for the "Reject" action
        Intent rejectIntent = new Intent(this, CallActionReceiver.class);
        rejectIntent.setAction("REJECT_CALL");
        rejectIntent.putExtra("caller_email", callerEmail);
        PendingIntent rejectPending = PendingIntent.getBroadcast(
            this, 11, rejectIntent, 
            PendingIntent.FLAG_UPDATE_CURRENT | PendingIntent.FLAG_IMMUTABLE
        );

        // Create a NEW Intent just for the Full-Screen Lock Screen popup
        Intent fullScreenIntent = new Intent(this, MainActivity.class);
        fullScreenIntent.setAction("SHOW_INCOMING_CALL"); // Distinct action that won't trigger the accept logic
        fullScreenIntent.putExtra("room_id", roomId);
        fullScreenIntent.putExtra("caller_email", callerEmail);
        fullScreenIntent.putExtra("caller_name", roomName);
        fullScreenIntent.addFlags(Intent.FLAG_ACTIVITY_NEW_TASK | Intent.FLAG_ACTIVITY_CLEAR_TOP | Intent.FLAG_ACTIVITY_SINGLE_TOP);

        PendingIntent fullScreenPending = PendingIntent.getActivity(
            this, 12, fullScreenIntent, // Use a different request code (e.g., 12)
            PendingIntent.FLAG_UPDATE_CURRENT | PendingIntent.FLAG_IMMUTABLE
        );

        Uri ringtoneUri = RingtoneManager.getDefaultUri(RingtoneManager.TYPE_RINGTONE);

        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.O) {
            NotificationChannel channel = new NotificationChannel(channelId, "Incoming Calls", NotificationManager.IMPORTANCE_HIGH);
            AudioAttributes audioAttributes = new AudioAttributes.Builder()
                    .setContentType(AudioAttributes.CONTENT_TYPE_SONIFICATION)
                    .setUsage(AudioAttributes.USAGE_NOTIFICATION_RINGTONE)
                    .build();
            channel.setSound(ringtoneUri, audioAttributes);
            notificationManager.createNotificationChannel(channel);
        }

        NotificationCompat.Builder builder = new NotificationCompat.Builder(this, channelId)
                .setSmallIcon(android.R.drawable.ic_menu_call) // Optional: replace with R.drawable.icon
                .setContentTitle("Incoming Call")
                .setContentText("Call from " + roomName)
                .setPriority(NotificationCompat.PRIORITY_HIGH)
                .setCategory(NotificationCompat.CATEGORY_CALL)
                .setContentIntent(fullScreenPending) // Tapping the notification body accepts the call
                .setFullScreenIntent(fullScreenPending, true)
                .setSound(ringtoneUri)
                .setOngoing(true)
                .setAutoCancel(true)
                // Add the actual UI buttons to the notification
                .addAction(android.R.drawable.ic_menu_call, "Accept", acceptPending)
                .addAction(android.R.drawable.ic_menu_close_clear_cancel, "Reject", rejectPending);

        Notification notification = builder.build();
        notification.flags |= Notification.FLAG_INSISTENT; // Keeps the ringtone looping
        notificationManager.notify(INCOMING_CALL_NOTIF_ID, notification);
    }
}
